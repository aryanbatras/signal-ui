/**
 * Button Contract
 *
 * Guarantees:
 * - semantic <button> element
 * - keyboard operable
 * - visible focus
 * - disabled prevents interaction
 * - loading prevents double submit
 *
 * Dimensions (Signal Layers):
 * - semantic (meaning)
 * - visual (color language)
 * - surface (solid, soft, outline, ghost)
 * - size (density)
 * - shape
 * - composition (icon, spinner)
 * - state (disabled, loading, active)
 * - behavior (motion, feedback)
 * - layout (block / inline)
 *
 * Forbidden:
 * - pointer-only interaction
 * - motion-only affordances
 */
